package jnorm.core.model;

public class JimpleClass {
    private String jimple;
    private String className;

    public JimpleClass(String jimple, String className) {
        this.jimple = jimple;
        this.className = className;
    }

    public String getJimple() {
        return jimple;
    }

    public String getClassName() {
        return className;
    }
}
